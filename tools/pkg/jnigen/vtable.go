package jnigen

import (
	"fmt"
	"strings"
)

// GenerateVtableHelpers produces C code for cgo_vtable_dispatch.h.
// Each function gets a static inline C helper that dereferences the vtable.
func GenerateVtableHelpers(funcs []MergedCapiFunc) string {
	var b strings.Builder

	b.WriteString("// Code generated by jnigen. DO NOT EDIT.\n\n")
	b.WriteString("#ifndef JNI_CGO_VTABLE_DISPATCH_H\n")
	b.WriteString("#define JNI_CGO_VTABLE_DISPATCH_H\n\n")
	b.WriteString("#include <jni.h>\n\n")

	for _, f := range funcs {
		writeVtableHelper(&b, f)
		b.WriteString("\n")
	}

	b.WriteString("#endif // JNI_CGO_VTABLE_DISPATCH_H\n")
	return b.String()
}

func writeVtableHelper(b *strings.Builder, f MergedCapiFunc) {
	// Determine the vtable pointer variable name.
	vtableVar := "env"
	if f.Vtable == "JavaVM" {
		vtableVar = "vm"
	}

	// Build parameter list for the helper function signature.
	var sigParams []string
	for _, p := range f.Params {
		sigParams = append(sigParams, fmt.Sprintf("%s %s", p.CType, p.CName))
	}

	// Build argument list for the vtable call (all params including env/vm).
	var callArgs []string
	for _, p := range f.Params {
		callArgs = append(callArgs, p.CName)
	}

	retType := f.Returns
	if f.IsVoid {
		retType = "void"
	}

	fmt.Fprintf(b, "static inline %s %s(%s) {\n",
		retType, f.HelperName, strings.Join(sigParams, ", "))

	callExpr := fmt.Sprintf("(*%s)->%s(%s)",
		vtableVar, f.CName, strings.Join(callArgs, ", "))

	if f.IsVoid {
		fmt.Fprintf(b, "    %s;\n", callExpr)
	} else {
		fmt.Fprintf(b, "    return %s;\n", callExpr)
	}

	b.WriteString("}\n")
}
