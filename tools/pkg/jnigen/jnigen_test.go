// TestFullPipeline runs a minimal spec through the entire pipeline:
// spec + overlay + templates -> output files.
func TestFullPipeline(t *testing.T) {
	specYAML := `
primitives:
  - { c_type: jboolean, go_type: uint8, suffix: Boolean }
  - { c_type: jint, go_type: int32, suffix: Int }

reference_types:
  - { c_type: jobject, parent: ~ }
  - { c_type: jclass, parent: jobject }

opaque_types:
  - { c_type: jmethodID }
  - { c_type: jfieldID }

function_families:
  - pattern: "Call{Type}MethodA"
    vtable: JNIEnv
    params: [jobject, jmethodID, "const jvalue*"]
    returns: "{type}"
    expand: [Int, Void]
    exception: true

env_functions:
  - { name: GetVersion, returns: jint }
  - { name: FindClass, params: ["const char*"], returns: jclass, exception: true }

vm_functions:
  - { name: DestroyJavaVM, returns: jint }

constants:
  - { name: JNI_OK, value: 0, go_type: int32 }
  - { name: JNI_ERR, value: -1, go_type: int32 }
`

	overlayYAML := `
type_renames:
  jobject: Object
  jclass: Class
  jmethodID: MethodID
  jfieldID: FieldID
  jint: int32
  jboolean: uint8

receivers:
  env_functions: "*Env"
  function_families: "*Env"
  vm_functions: "*VM"

functions:
  GetVersion:
    go_name: GetVersion
    returns: {go_type: int32}
  FindClass:
    go_name: FindClass
    check_exception: true
    params:
      - {name: name, c_type: "const char*", go_type: string}
    returns: {go_type: "*Class"}
  DestroyJavaVM:
    go_name: Destroy
    returns: {go_type: error, transform: jni_error}

family_overlays:
  "Call{Type}MethodA":
    go_pattern: "Call{Type}Method"
    check_exception: true
    params:
      - {name: obj, c_type: jobject, go_type: "*Object"}
      - {name: method, c_type: jmethodID, go_type: MethodID}
      - {name: args, c_type: "const jvalue*", go_type: "...Value"}
    returns:
      primitive: "{go_type}"
      Void: ~

param_transforms: {}
`

	dir := t.TempDir()
	specPath := filepath.Join(dir, "spec.yaml")
	overlayPath := filepath.Join(dir, "overlay.yaml")
	tmplDir := filepath.Join(dir, "templates")
	outputDir := filepath.Join(dir, "output")

	if err := os.WriteFile(specPath, []byte(specYAML), 0o644); err != nil {
		t.Fatal(err)
	}
	if err := os.WriteFile(overlayPath, []byte(overlayYAML), 0o644); err != nil {
		t.Fatal(err)
	}
	if err := os.MkdirAll(tmplDir, 0o755); err != nil {
		t.Fatal(err)
	}
	if err := os.MkdirAll(filepath.Join(outputDir, "capi"), 0o755); err != nil {
		t.Fatal(err)
	}

	// Create minimal templates for the pipeline test.
	templates := map[string]string{
		"capi_vtable_dispatch.go.tmpl": `// Code generated by jnigen. DO NOT EDIT.

#ifndef JNI_CGO_VTABLE_DISPATCH_H
#define JNI_CGO_VTABLE_DISPATCH_H

#include <jni.h>
{{range .AllFunctions}}
static inline {{.Returns}} {{.HelperName}}({{range $i, $p := .Params}}{{if $i}}, {{end}}{{$p.CType}} {{$p.CName}}{{end}}) {
{{- if .IsVoid}}
    (*{{(index .Params 0).CName}})->{{.CName}}({{range $i, $p := .Params}}{{if $i}}, {{end}}{{$p.CName}}{{end}});
{{- else}}
    return (*{{(index .Params 0).CName}})->{{.CName}}({{range $i, $p := .Params}}{{if $i}}, {{end}}{{$p.CName}}{{end}});
{{- end}}
}
{{end}}
#endif // JNI_CGO_VTABLE_DISPATCH_H
`,
		"capi_functions.go.tmpl": `// Code generated by jnigen. DO NOT EDIT.

package capi

// #include "cgo_vtable_dispatch.h"
import "C"
{{range .AllFunctions}}
// {{.CName}} wraps the JNI {{.CName}} function via vtable dispatch.
func {{.CName}}({{range $i, $p := .Params}}{{if $i}}, {{end}}{{$p.CName}} {{cTypeToGo $p.CType}}{{end}}){{if not .IsVoid}} {{cTypeToGo .Returns}}{{end}} {
	{{if .IsVoid}}C.{{.HelperName}}({{range $i, $p := .Params}}{{if $i}}, {{end}}{{$p.CName}}{{end}}){{else}}return C.{{.HelperName}}({{range $i, $p := .Params}}{{if $i}}, {{end}}{{$p.CName}}{{end}}){{end}}
}
{{end}}
`,
		"capi_types.go.tmpl": `// Code generated by jnigen. DO NOT EDIT.

package capi

// #include <jni.h>
import "C"

{{range .Types}}type {{.GoType}} = C.{{.CType}}
{{end}}
{{range .OpaqueTypes}}type {{.GoType}} = C.{{.CType}}
{{end}}
type Jvalue = C.jvalue
`,
		"capi_const.go.tmpl": `// Code generated by jnigen. DO NOT EDIT.

package capi

// #include <jni.h>
import "C"

const (
{{range .Constants}}	{{.GoName}} = C.{{.CName}}
{{end}})
`,
		"capi_jvalue_access.go.tmpl": `// Code generated by jnigen. DO NOT EDIT.

package capi

// #include <jni.h>
import "C"
import "unsafe"

var _ = unsafe.Pointer(nil)
{{range .Primitives}}
func JvalueSet{{.Suffix}}(v *C.jvalue, val C.{{.CType}}) {
	*(*C.{{.CType}})(unsafe.Pointer(v)) = val
}
{{end}}
`,
		"env.go.tmpl": `// Code generated by jnigen. DO NOT EDIT.

package jni

import (
	"unsafe"
	"github.com/AndroidGoLab/jni/capi"
	"github.com/AndroidGoLab/jni/internal/jnierr"
)

var _ = unsafe.Pointer(nil)
var _ = capi.JNI_OK
var _ = jnierr.CheckException

type Env struct { ptr *capi.JNIEnv }
{{range .Methods}}
func (e *Env) {{.GoName}}({{.GoParamList}}) {{.GoReturnList}} { panic("stub") }
{{end}}
`,
		"vm.go.tmpl": `// Code generated by jnigen. DO NOT EDIT.

package jni

import "github.com/AndroidGoLab/jni/capi"

type VM struct { ptr *capi.JavaVM }
{{range .Methods}}
func (vm *VM) {{.GoName}}({{.GoParamList}}) {{.GoReturnList}} { panic("stub") }
{{end}}
`,
		"type_file.go.tmpl": `// Code generated by jnigen. DO NOT EDIT.

package jni

import "github.com/AndroidGoLab/jni/capi"

var _ = capi.JNI_OK
{{range .Types}}
type {{.GoName}} struct {
{{- if .Parent}}
	{{.Parent.GoName}}
{{- else}}
	ref capi.Jobject
{{- end}}
}
{{end}}
`,
		"jvalue.go.tmpl": `// Code generated by jnigen. DO NOT EDIT.

package jni

import "github.com/AndroidGoLab/jni/capi"

type Value struct { val capi.Jvalue }
{{range .Primitives}}
func {{.GoName}}Value(v {{.GoType}}) Value { return Value{} }
{{end}}
`,
		"errors.go.tmpl": `// Code generated by jnigen. DO NOT EDIT.

package jni

import "fmt"

type Error int32
func (e Error) Error() string { return fmt.Sprintf("jni: error %d", int32(e)) }
{{range .ErrorCodes}}
const {{.GoName}} = Error({{.Value}})
{{end}}
`,
		"types.go.tmpl": `// Code generated by jnigen. DO NOT EDIT.

package jni

import "github.com/AndroidGoLab/jni/capi"

var _ = capi.JNI_OK
`,
		"method_id.go.tmpl": `// Code generated by jnigen. DO NOT EDIT.

package jni

import "github.com/AndroidGoLab/jni/capi"

type MethodID = capi.JmethodID
`,
		"field_id.go.tmpl": `// Code generated by jnigen. DO NOT EDIT.

package jni

import "github.com/AndroidGoLab/jni/capi"

type FieldID = capi.JfieldID
`,
		"object_ref_type.go.tmpl": `// Code generated by jnigen. DO NOT EDIT.

package jni

import "github.com/AndroidGoLab/jni/capi"

type ObjectRefType = capi.Jint
`,
		"capi_type_file.go.tmpl": `// Code generated by jnigen. DO NOT EDIT.

package capi

// #include <jni.h>
import "C"

{{range .Types}}type {{.GoType}} = C.{{.CType}}
{{end}}
`,
	}

	for name, content := range templates {
		path := filepath.Join(tmplDir, name)
		if err := os.WriteFile(path, []byte(content), 0o644); err != nil {
			t.Fatalf("writing template %s: %v", name, err)
		}
	}

	// Run the pipeline.
	spec, err := LoadSpec(specPath)
	if err != nil {
		t.Fatalf("LoadSpec: %v", err)
	}

	overlay, err := LoadOverlay(overlayPath)
	if err != nil {
		t.Fatalf("LoadOverlay: %v", err)
	}

	merged, err := Merge(spec, overlay)
	if err != nil {
		t.Fatalf("Merge: %v", err)
	}

	if err := Render(merged, tmplDir, outputDir); err != nil {
		t.Fatalf("Render: %v", err)
	}

	// Verify cgo_vtable_dispatch.h was generated and contains expected content.
	vtableH := readOutputFile(t, outputDir, "capi", "cgo_vtable_dispatch.h")
	if !strings.Contains(vtableH, "jni_FindClass") {
		t.Error("cgo_vtable_dispatch.h missing jni_FindClass")
	}
	if !strings.Contains(vtableH, "jni_CallIntMethodA") {
		t.Error("cgo_vtable_dispatch.h missing jni_CallIntMethodA")
	}
	if !strings.Contains(vtableH, "jni_CallVoidMethodA") {
		t.Error("cgo_vtable_dispatch.h missing jni_CallVoidMethodA")
	}
	if !strings.Contains(vtableH, "jni_DestroyJavaVM") {
		t.Error("cgo_vtable_dispatch.h missing jni_DestroyJavaVM")
	}
	if !strings.Contains(vtableH, "static inline") {
		t.Error("cgo_vtable_dispatch.h missing static inline vtable dispatch functions")
	}
	if !strings.Contains(vtableH, "Code generated by jnigen") {
		t.Error("cgo_vtable_dispatch.h missing generated header")
	}

	// Verify capi/jni.go is valid Go (can't actually compile CGo in tests).
	jniGo := readOutputFile(t, outputDir, "capi", "jni.go")
	if !strings.Contains(jniGo, "package capi") {
		t.Error("jni.go missing package declaration")
	}
	if !strings.Contains(jniGo, "Code generated by jnigen") {
		t.Error("jni.go missing generated header")
	}

	// Verify per-type capi files exist (one type per file).
	objectGo := readOutputFile(t, outputDir, "capi", "object.go")
	if !strings.Contains(objectGo, "package capi") {
		t.Error("capi/object.go missing package declaration")
	}

	// Verify const.go exists.
	constGo := readOutputFile(t, outputDir, "capi", "const.go")
	if !strings.Contains(constGo, "JNI_OK") {
		t.Error("const.go missing JNI_OK")
	}
}

// TestFullPipeline_RealSpecs tests the pipeline with the real spec and overlay files.
func TestFullPipeline_RealSpecs(t *testing.T) {
	repoRoot := findRepoRoot(t)

	specPath := filepath.Join(repoRoot, "spec", "jni.yaml")
	overlayPath := filepath.Join(repoRoot, "spec", "overlays", "jni.yaml")
	tmplDir := filepath.Join(repoRoot, "templates", "jni")

	if _, err := os.Stat(specPath); os.IsNotExist(err) {
		t.Skip("real spec not found")
	}

	spec, err := LoadSpec(specPath)
	if err != nil {
		t.Fatalf("LoadSpec: %v", err)
	}

	overlay, err := LoadOverlay(overlayPath)
	if err != nil {
		t.Fatalf("LoadOverlay: %v", err)
	}

	merged, err := Merge(spec, overlay)
	if err != nil {
		t.Fatalf("Merge: %v", err)
	}

	// Verify we have a substantial number of capi functions.
	if len(merged.CapiFunctions) < 100 {
		t.Errorf("expected 100+ capi functions, got %d", len(merged.CapiFunctions))
	}

	// Verify env methods were generated.
	if len(merged.EnvMethods) < 50 {
		t.Errorf("expected 50+ env methods, got %d", len(merged.EnvMethods))
	}

	// Verify VM methods.
	if len(merged.VMMethods) < 3 {
		t.Errorf("expected 3+ vm methods, got %d", len(merged.VMMethods))
	}

	// Render to temp directory.
	outputDir := t.TempDir()
	if err := os.MkdirAll(filepath.Join(outputDir, "capi"), 0o755); err != nil {
		t.Fatal(err)
	}

	if err := Render(merged, tmplDir, outputDir); err != nil {
		t.Fatalf("Render: %v", err)
	}

	// Verify the C header has the expected pattern.
	vtableH := readOutputFile(t, outputDir, "capi", "cgo_vtable_dispatch.h")
	if !strings.Contains(vtableH, "(*env)->FindClass(env") {
		t.Error("cgo_vtable_dispatch.h missing FindClass vtable pattern")
	}
	if !strings.Contains(vtableH, "(*vm)->DestroyJavaVM(vm)") {
		t.Error("cgo_vtable_dispatch.h missing DestroyJavaVM vtable pattern")
	}

	// Count static inline vtable dispatch functions — should be 100+ for all JNI functions.
	dispatchCount := strings.Count(vtableH, "static inline")
	if dispatchCount < 100 {
		t.Errorf("expected 100+ static inline vtable dispatch functions, got %d", dispatchCount)
	}

	// Verify generated Go files parse (even though we can't compile CGo).
	goFiles := []string{
		filepath.Join(outputDir, "capi", "object.go"),
		filepath.Join(outputDir, "capi", "const.go"),
		filepath.Join(outputDir, "capi", "cgo_jvalue_access.go"),
	}

	fset := token.NewFileSet()
	for _, goFile := range goFiles {
		if _, err := os.Stat(goFile); os.IsNotExist(err) {
			t.Errorf("expected file %s to exist", goFile)
			continue
		}
		// Parse with all mode to allow import "C".
		_, err := parser.ParseFile(fset, goFile, nil, parser.AllErrors|parser.ParseComments)
		if err != nil {
			// CGo import "C" causes parse errors in standard parser; that's expected.
			if !strings.Contains(err.Error(), `expected 'STRING'`) {
				t.Errorf("parsing %s: %v", goFile, err)
			}
		}
	}
}

func readOutputFile(t *testing.T, parts ...string) string {
	t.Helper()
	path := filepath.Join(parts...)
	data, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("reading %s: %v", path, err)
	}
	return string(data)
}
