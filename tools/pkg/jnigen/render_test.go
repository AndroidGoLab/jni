func TestRenderFile_GoFormat(t *testing.T) {
	dir := t.TempDir()
	tmplDir := filepath.Join(dir, "templates")
	if err := os.MkdirAll(tmplDir, 0o755); err != nil {
		t.Fatal(err)
	}

	// Write a simple template that produces valid Go.
	tmplContent := `// Code generated by jnigen. DO NOT EDIT.

package test

const X = 42
`
	if err := os.WriteFile(filepath.Join(tmplDir, "test.go.tmpl"), []byte(tmplContent), 0o644); err != nil {
		t.Fatal(err)
	}

	// Create a minimal merged spec.
	merged := &MergedSpec{
		TypeMethods: make(map[string][]MergedMethod),
	}

	// We can't easily test Render directly without all templates,
	// so test the buildFuncMap and cTypeToGo helper instead.
	_ = merged

	fm := buildFuncMap()
	if fm == nil {
		t.Fatal("buildFuncMap returned nil")
	}
}

func TestCTypeToGo(t *testing.T) {
	tests := []struct {
		input    string
		expected string
	}{
		{"jint", "C.jint"},
		{"jclass", "C.jclass"},
		{"jobject", "C.jobject"},
		{"const char*", "*C.char"},
		{"void*", "unsafe.Pointer"},
		{"void", ""},
		{"JNIEnv*", "*C.JNIEnv"},
		{"const jvalue*", "*C.jvalue"},
		{"jboolean*", "*C.jboolean"},
		{"JNIEnv**", "**C.JNIEnv"},
		{"void**", "*unsafe.Pointer"},
	}

	for _, tt := range tests {
		t.Run(tt.input, func(t *testing.T) {
			got := cTypeToGo(tt.input)
			if got != tt.expected {
				t.Errorf("cTypeToGo(%q) = %q, want %q", tt.input, got, tt.expected)
			}
		})
	}
}

func TestBuildFuncMap_Helpers(t *testing.T) {
	fm := buildFuncMap()

	// Test camelCase.
	cc := fm["camelCase"].(func(string) string)
	if got := cc("hello_world"); got != "HelloWorld" {
		t.Errorf("camelCase(hello_world) = %q, want HelloWorld", got)
	}

	// Test lowerCamel.
	lc := fm["lowerCamel"].(func(string) string)
	if got := lc("hello_world"); got != "helloWorld" {
		t.Errorf("lowerCamel(hello_world) = %q, want helloWorld", got)
	}

	// Test isVoid.
	iv := fm["isVoid"].(func(string) bool)
	if !iv("void") {
		t.Error("isVoid(void) should be true")
	}
	if !iv("") {
		t.Error("isVoid('') should be true")
	}
	if iv("jint") {
		t.Error("isVoid(jint) should be false")
	}

	// Test join.
	j := fm["join"].(func([]string, string) string)
	if got := j([]string{"a", "b"}, ", "); got != "a, b" {
		t.Errorf("join = %q, want 'a, b'", got)
	}

	// Test add.
	addFn := fm["add"].(func(int, int) int)
	if got := addFn(3, 4); got != 7 {
		t.Errorf("add(3,4) = %d, want 7", got)
	}

	// Test sub.
	subFn := fm["sub"].(func(int, int) int)
	if got := subFn(10, 3); got != 7 {
		t.Errorf("sub(10,3) = %d, want 7", got)
	}

	// Test goParamName.
	gpn := fm["goParamName"].(func(string) string)
	if got := gpn("p0"); got != "p0" {
		t.Errorf("goParamName(p0) = %q, want p0", got)
	}

	// Test trimSuffix.
	ts := fm["trimSuffix"].(func(string, string) string)
	if got := ts("hello.go", ".go"); got != "hello" {
		t.Errorf("trimSuffix = %q, want hello", got)
	}

	// Test hasPrefix.
	hp := fm["hasPrefix"].(func(string, string) bool)
	if !hp("hello", "hel") {
		t.Error("hasPrefix(hello, hel) should be true")
	}

	// Test hasSuffix.
	hs := fm["hasSuffix"].(func(string, string) bool)
	if !hs("hello", "llo") {
		t.Error("hasSuffix(hello, llo) should be true")
	}

	// Test trimPrefix.
	tp := fm["trimPrefix"].(func(string, string) string)
	if got := tp("hello", "hel"); got != "lo" {
		t.Errorf("trimPrefix = %q, want lo", got)
	}

	// Test upper.
	up := fm["upper"].(func(string) string)
	if got := up("abc"); got != "ABC" {
		t.Errorf("upper = %q, want ABC", got)
	}

	// Test lower.
	lo := fm["lower"].(func(string) string)
	if got := lo("ABC"); got != "abc" {
		t.Errorf("lower = %q, want abc", got)
	}

	// Test title.
	ti := fm["title"].(func(string) string)
	if got := ti("hello"); got != "Hello" {
		t.Errorf("title = %q, want Hello", got)
	}

	// Test capitalizeFirst.
	cf := fm["capitalizeFirst"].(func(string) string)
	if got := cf("hello"); got != "Hello" {
		t.Errorf("capitalizeFirst = %q, want Hello", got)
	}

	// Test cTypeToGo.
	ctg := fm["cTypeToGo"].(func(string) string)
	if got := ctg("jint"); got != "C.jint" {
		t.Errorf("cTypeToGo = %q, want C.jint", got)
	}
}

func TestRenderFile_InvalidGo(t *testing.T) {
	dir := t.TempDir()
	// Template produces invalid Go source that gofmt will reject.
	tmpl := template.Must(template.New("bad.go.tmpl").Parse(`package test
func { invalid syntax
`))
	outputPath := filepath.Join(dir, "bad.go")
	err := renderFile(tmpl, "bad.go.tmpl", outputPath, nil, false)
	if err == nil {
		t.Fatal("expected error for invalid Go source")
	}
	if !strings.Contains(err.Error(), "formatting") {
		t.Errorf("expected formatting error, got: %v", err)
	}
	// The unformatted file should be saved for debugging.
	debugPath := outputPath + ".unformatted"
	if _, statErr := os.Stat(debugPath); os.IsNotExist(statErr) {
		t.Error("expected unformatted debug file to exist")
	}
}

func TestRenderFile_Header(t *testing.T) {
	dir := t.TempDir()
	// C header content (not Go) -- should skip gofmt.
	tmpl := template.Must(template.New("test.h.go.tmpl").Parse(`#ifndef TEST_H
#define TEST_H
static inline void foo() {}
#endif
`))
	outputPath := filepath.Join(dir, "test.h")
	err := renderFile(tmpl, "test.h.go.tmpl", outputPath, nil, true)
	if err != nil {
		t.Fatalf("renderFile header: %v", err)
	}
	data, readErr := os.ReadFile(outputPath)
	if readErr != nil {
		t.Fatal(readErr)
	}
	if !strings.Contains(string(data), "#ifndef TEST_H") {
		t.Error("header content missing")
	}
}

func TestRenderFile_ValidGo(t *testing.T) {
	dir := t.TempDir()
	tmpl := template.Must(template.New("good.go.tmpl").Parse(`package test

const X = 42
`))
	outputPath := filepath.Join(dir, "good.go")
	err := renderFile(tmpl, "good.go.tmpl", outputPath, nil, false)
	if err != nil {
		t.Fatalf("renderFile valid Go: %v", err)
	}
	data, readErr := os.ReadFile(outputPath)
	if readErr != nil {
		t.Fatal(readErr)
	}
	if !strings.Contains(string(data), "const X = 42") {
		t.Error("expected formatted Go output")
	}
}

