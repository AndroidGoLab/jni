package javagen

import (
	"go/parser"
	"go/token"
	"strings"
	"testing"

	"github.com/stretchr/testify/assert"
	"github.com/stretchr/testify/require"
)

func TestPackagePrefix(t *testing.T) {
	tests := []struct {
		pkg    string
		expect string
	}{
		{"app", "App"},
		{"location", "Location"},
		{"wifi_p2p", "WifiP2p"},
		{"bluetooth_le", "BluetoothLe"},
		{"net", "Net"},
	}
	for _, tt := range tests {
		t.Run(tt.pkg, func(t *testing.T) {
			assert.Equal(t, tt.expect, packagePrefix(tt.pkg))
		})
	}
}

func TestBuildConsolidatedSource(t *testing.T) {
	packages := []PackageConstants{
		{
			PackageName: "app",
			Constants: []Constant{
				{GoName: "LocationService", Value: `"location"`, GoType: "string"},
				{GoName: "CameraService", Value: `"camera"`, GoType: "string"},
			},
		},
		{
			PackageName: "location",
			Constants: []Constant{
				{GoName: "GpsProvider", Value: `"gps"`, GoType: "string"},
				{GoName: "NetworkProvider", Value: `"network"`, GoType: "string"},
				{GoName: "Creator", Value: "0", GoType: "int"},
			},
		},
	}

	src := buildConsolidatedSource(packages)

	// Verify it is valid Go source.
	fset := token.NewFileSet()
	_, err := parser.ParseFile(fset, "constants.go", src, parser.AllErrors)
	require.NoError(t, err, "generated source is not valid Go:\n%s", src)

	// Verify constants are prefixed.
	assert.Contains(t, src, "AppLocationService")
	assert.Contains(t, src, "AppCameraService")
	assert.Contains(t, src, "LocationGpsProvider")
	assert.Contains(t, src, "LocationNetworkProvider")
	assert.Contains(t, src, "LocationCreator")

	// Verify unprefixed names do not appear (proves disambiguation).
	// "LocationService" alone could be a substring of "AppLocationService",
	// so we check that the bare form without prefix does not appear as a
	// standalone constant declaration.
	assert.NotContains(t, src, "\tLocationService ")
	assert.NotContains(t, src, "\tCameraService ")
	assert.NotContains(t, src, "\tGpsProvider ")

	// Verify generated header.
	assert.True(t, strings.HasPrefix(src, "// Code generated by javagen. DO NOT EDIT."))

	// Verify package declaration.
	assert.Contains(t, src, "package android")
}

func TestBuildConsolidatedSourceEmpty(t *testing.T) {
	src := buildConsolidatedSource(nil)

	fset := token.NewFileSet()
	_, err := parser.ParseFile(fset, "constants.go", src, parser.AllErrors)
	require.NoError(t, err, "empty consolidated source is not valid Go:\n%s", src)

	assert.Contains(t, src, "package android")
}

func TestBuildConsolidatedSourceTypedConstants(t *testing.T) {
	packages := []PackageConstants{
		{
			PackageName: "vibrator",
			Constants: []Constant{
				{GoName: "EffectClick", Value: "0", GoType: "Effect", GoUnderlying: "int"},
				{GoName: "EffectDoubleClick", Value: "1", GoType: "Effect", GoUnderlying: "int"},
			},
		},
	}

	src := buildConsolidatedSource(packages)

	fset := token.NewFileSet()
	_, err := parser.ParseFile(fset, "constants.go", src, parser.AllErrors)
	require.NoError(t, err, "typed constants source is not valid Go:\n%s", src)

	// Typed constants get prefixed type name.
	assert.Contains(t, src, "type VibratorEffect int")
	assert.Contains(t, src, "VibratorEffectClick VibratorEffect = 0")
	assert.Contains(t, src, "VibratorEffectDoubleClick VibratorEffect = 1")
}
