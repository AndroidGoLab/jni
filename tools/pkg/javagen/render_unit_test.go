func TestGenerate_NoTemplates(t *testing.T) {
	specYAML := `
package: test
go_import: "github.com/example/test"
classes:
  - java_class: com.example.Foo
    go_type: Foo
    obtain: system_service
    service_name: "foo"
    methods:
      - java_method: doIt
        go_name: DoIt
        params: []
        returns: void
`
	specPath := writeTempYAML(t, specYAML)
	tmplDir := t.TempDir()
	outputDir := t.TempDir()

	// Generate with empty template dir: shouldRender returns true for
	// methods.go.tmpl but the template is nil, so it gets skipped.
	err := Generate(specPath, "/nonexistent/overlay.yaml", tmplDir, outputDir, testGoModule)
	// Should succeed because shouldRender is true but templates.Lookup returns nil, so we skip.
	if err != nil {
		t.Fatalf("Generate with no templates should not error: %v", err)
	}
}
