func TestCleanStaleFiles(t *testing.T) {
	// Test on nonexistent directory (should not panic).
	cleanStaleFiles("/nonexistent/dir")

	// Test on a directory with generated and non-generated files.
	dir := t.TempDir()

	// Write a generated file.
	generated := "// Code generated by javagen. DO NOT EDIT.\n\npackage test\n"
	if err := os.WriteFile(filepath.Join(dir, "gen.go"), []byte(generated), 0o644); err != nil {
		t.Fatal(err)
	}

	// Write a non-generated file.
	handWritten := "package test\n\nfunc foo() {}\n"
	if err := os.WriteFile(filepath.Join(dir, "manual.go"), []byte(handWritten), 0o644); err != nil {
		t.Fatal(err)
	}

	// Write a non-Go file (should be ignored).
	if err := os.WriteFile(filepath.Join(dir, "readme.md"), []byte("# test"), 0o644); err != nil {
		t.Fatal(err)
	}

	cleanStaleFiles(dir)

	// gen.go should be removed.
	if _, err := os.Stat(filepath.Join(dir, "gen.go")); !os.IsNotExist(err) {
		t.Error("expected gen.go to be removed")
	}

	// manual.go should remain.
	if _, err := os.Stat(filepath.Join(dir, "manual.go")); err != nil {
		t.Error("manual.go should not be removed")
	}

	// readme.md should remain.
	if _, err := os.Stat(filepath.Join(dir, "readme.md")); err != nil {
		t.Error("readme.md should not be removed")
	}
}

