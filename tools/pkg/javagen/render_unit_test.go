func TestShouldRender(t *testing.T) {
	// A spec with everything.
	full := &MergedSpec{
		Classes: []MergedClass{
			{
				GoType:      "Manager",
				Obtain:      "system_service",
				ServiceName: "test",
				Methods: []MergedMethod{
					{GoName: "DoIt"},
				},
			},
		},
		DataClasses: []MergedDataClass{
			{GoType: "Point"},
		},
		Callbacks: []MergedCallback{
			{GoType: "Listener"},
		},
		ConstantGroups: []MergedConstantGroup{
			{Values: []MergedConstant{{GoName: "X", Value: "1"}}},
		},
	}

	for _, tmpl := range templateOrder {
		if !shouldRender(tmpl, full) {
			t.Errorf("shouldRender(%q, full) = false, want true", tmpl)
		}
	}

	// An empty spec (only package info).
	empty := &MergedSpec{
		Package: "empty",
	}

	if shouldRender("callbacks.go.tmpl", empty) {
		t.Error("shouldRender(callbacks.go.tmpl, empty) should be false")
	}
	if shouldRender("constants.go.tmpl", empty) {
		t.Error("shouldRender(constants.go.tmpl, empty) should be false")
	}

	// package.go.tmpl and init.go.tmpl always render.
	if !shouldRender("package.go.tmpl", empty) {
		t.Error("shouldRender(package.go.tmpl, empty) should be true")
	}
	if !shouldRender("init.go.tmpl", empty) {
		t.Error("shouldRender(init.go.tmpl, empty) should be true")
	}
}

